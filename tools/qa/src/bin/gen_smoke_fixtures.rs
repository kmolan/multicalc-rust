//! Emits `tools/embedded-smoke/src/fixtures.rs`: a `no_std` file of `const`
//! goldens for the on-target smoke checks, taken bit-exact from the committed
//! QA fixtures. The firmware asserts against the identical values the host
//! goldens hold, so the two share one source of truth.
//!
//! Run `cargo run -p multicalc-qa --bin gen_smoke_fixtures`. The output is
//! deterministic (fixed ordering and formatting), so CI can regenerate it and
//! diff against the committed copy.

use std::fmt::Write as _;
use std::path::Path;

use multicalc_qa::load::load_dir;
use multicalc_qa::schema::{Fixture, Tol};

fn find<'a>(fixtures: &'a [Fixture], case: &str) -> &'a Fixture {
    fixtures
        .iter()
        .find(|f| f.case == case)
        .unwrap_or_else(|| unreachable!("fixture case {case:?} not found"))
}

/// The `f64` tolerance for a fixture. Goldens are shared across ABIs, so a
/// per-ABI entry is preferred when present and the host tolerance is the
/// fallback.
fn f64_tol(fx: &Fixture) -> Tol {
    for key in ["f64/thumbv7em-eabi", "f64/thumbv7em-eabihf", "f64/thumbv6m"] {
        if let Some(t) = fx.tolerances.table.get(key) {
            return *t;
        }
    }
    *fx.tolerances
        .table
        .get("f64/host")
        .unwrap_or_else(|| unreachable!("fixture {:?} has no f64/host tolerance", fx.case))
}

/// A vector of `f64` as a bit-exact `[f64; N]` literal, one element per line.
fn vector_lit(data: &[f64]) -> String {
    let mut s = format!("[f64; {}] = [\n", data.len());
    for &x in data {
        let _ = writeln!(s, "    f64::from_bits(0x{:016x}),", x.to_bits());
    }
    s.push_str("];\n");
    s
}

/// A row-major matrix as a bit-exact `[[f64; C]; R]` literal.
fn matrix_lit(rows: usize, cols: usize, data: &[f64]) -> String {
    let mut s = format!("[[f64; {cols}]; {rows}] = [\n");
    for r in 0..rows {
        s.push_str("    [\n");
        for c in 0..cols {
            let _ = writeln!(
                s,
                "        f64::from_bits(0x{:016x}),",
                data[r * cols + c].to_bits()
            );
        }
        s.push_str("    ],\n");
    }
    s.push_str("];\n");
    s
}

fn main() {
    let linalg = load_dir("fixtures/v1/linalg");
    let optimization = load_dir("fixtures/v1/optimization");

    let svd = find(&linalg, "svd_3x3");
    let (r, c, a) = svd.inputs["A"].as_matrix();
    let sigma = svd.expected["singular_values"].as_vector();
    let svd_tol = f64_tol(svd);

    let rosen = find(&optimization, "rosenbrock");
    let x0 = rosen.inputs["x0"].as_vector();
    let solution = rosen.expected["solution"].as_vector();
    let rosen_tol = f64_tol(rosen);

    let root_finding = load_dir("fixtures/v1/root_finding");
    let wien = find(&root_finding, "wien_newton");
    let wien_x0 = wien.inputs["start"].as_scalar();
    let wien_root = wien.expected["root"].as_scalar();
    let wien_tol = f64_tol(wien);

    let mut out = String::new();
    out.push_str(
        "// GENERATED by `cargo run -p multicalc-qa --bin gen_smoke_fixtures`. Do not edit.\n\
         // Goldens for the on-target smoke checks, taken bit-exact from tools/qa/fixtures/v1.\n\
         // SVD golden: linalg/svd_3x3. LM golden: optimization/rosenbrock.\n\
         // Wien golden: root_finding/wien_newton.\n\
         // Reduced (canary) builds use a subset, so some consts may be unused.\n\
         #![allow(clippy::unreadable_literal)]\n\
         #![allow(dead_code)]\n\n",
    );

    let _ = write!(
        out,
        "/// Input matrix `A` (row-major {r}\u{d7}{c}) for the SVD check, from linalg/svd_3x3.\n\
         pub const SVD_3X3_INPUT: {}\n",
        matrix_lit(r, c, &a),
    );
    let _ = write!(
        out,
        "/// Expected singular values of `SVD_3X3_INPUT`, descending.\n\
         pub const SVD_3X3_SINGULAR_VALUES: {}\n",
        vector_lit(&sigma),
    );
    let _ = write!(
        out,
        "/// Comparison tolerance for the SVD check (from the fixture's f64 tolerance).\n\
         pub const SVD_3X3_ABS: f64 = {:e};\n\
         pub const SVD_3X3_REL: f64 = {:e};\n\n",
        svd_tol.abs, svd_tol.rel,
    );

    let _ = write!(
        out,
        "/// Initial guess for the Rosenbrock least-squares fit, from optimization/rosenbrock.\n\
         pub const ROSENBROCK_X0: {}\n",
        vector_lit(&x0),
    );
    let _ = write!(
        out,
        "/// Expected minimizer of the Rosenbrock fit.\n\
         pub const ROSENBROCK_SOLUTION: {}\n",
        vector_lit(&solution),
    );
    let _ = write!(
        out,
        "/// Comparison tolerance for the Rosenbrock check (from the fixture's f64 tolerance).\n\
         pub const ROSENBROCK_ABS: f64 = {:e};\n\
         pub const ROSENBROCK_REL: f64 = {:e};\n\n",
        rosen_tol.abs, rosen_tol.rel,
    );

    let _ = write!(
        out,
        "/// Newton start for the Wien check, from root_finding/wien_newton.\n\
         pub const ROOT_WIEN_X0: f64 = f64::from_bits(0x{:016x});\n\
         /// Expected Wien root (transcendental, so taken bit-exact from the fixture).\n\
         pub const ROOT_WIEN_ROOT: f64 = f64::from_bits(0x{:016x});\n\
         /// Comparison tolerance for the Wien check (from the fixture's f64 tolerance).\n\
         pub const ROOT_WIEN_ABS: f64 = {:e};\n\
         pub const ROOT_WIEN_REL: f64 = {:e};\n",
        wien_x0.to_bits(),
        wien_root.to_bits(),
        wien_tol.abs,
        wien_tol.rel,
    );

    let dest = Path::new(env!("CARGO_MANIFEST_DIR")).join("../embedded-smoke/src/fixtures.rs");
    std::fs::write(&dest, out).unwrap_or_else(|e| unreachable!("write {dest:?}: {e}"));
    println!("wrote {}", dest.display());
}
